'''
Assignment Operators are used to assign values to variables.
This operator is used to assign the value of the right side of the expression
to the left side operand.
'''


# Assigning values using
# Assignment Operator
a = 3
b = 5

c = a + b

# Output
print(c)


a = 3
b = 5

# a = a + b
a += b

# Output
print(a)